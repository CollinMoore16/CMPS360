package Mod2.basics;

public class loop {
    public static void main(String[] args) {

        // For Loop

        for(int i = 0; i < 10; i++) {
            System.out.println("Found a number of errors " + i);
        }

        //While Loop
        
        int i = 0;
        while (i < 10) {
            System.out.println("this is the number" + i);
            i++;
        } 
    }
}
